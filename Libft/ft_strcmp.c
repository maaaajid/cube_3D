#include "libft.h"

int	ft_strcmp(char *s1, char *s2, char c)
{
	while (*s1 && *s2 && *s1 == *s2 && *s1 != c && *s2 != c)
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
